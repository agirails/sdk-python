class TestCanonicalJsonByteExactness:
    @pytest.mark.parametrize("vid", list(CANONICAL_INPUTS.keys()))
    def test_canonical_string_matches_ts(self, vid: str, golden: dict) -> None:
        expected = golden["canonical"][vid]["canonical"]
        assert canonical_json_dumps(CANONICAL_INPUTS[vid]) == expected, (
            f"[{vid}] canonical JSON diverged from TS"
        )

    @pytest.mark.parametrize("vid", list(CANONICAL_INPUTS.keys()))
    def test_result_hash_matches_ts(self, vid: str, golden: dict) -> None:
        expected = golden["canonical"][vid]["resultHash"]
        assert compute_result_hash(CANONICAL_INPUTS[vid]) == expected, (
            f"[{vid}] computeResultHash diverged from TS"
        )

    def test_integer_valued_float_drops_fraction(self, golden: dict) -> None:
        # The canonical bug this wave fixes: 1.0 -> "1", -0.0 -> "0".
        assert canonical_json_dumps({"amount": 1.0}) == '{"amount":1}'
        assert canonical_json_dumps({"x": -0.0}) == '{"x":0}'
        assert canonical_json_dumps({"big": 1e16}) == '{"big":10000000000000000}'
        assert canonical_json_dumps({"small": 1e-7}) == '{"small":1e-7}'


class TestProofGeneratorKeccak:
    def test_hash_content_matches_ts_keccak(self, golden: dict) -> None:
        # hash_content().content_hash mirrors TS ProofGenerator.hashContent:
        # raw keccak256(utf8(content)), NOT canonical-JSON hashing.
        gen = ProofGenerator()  # default is now keccak256
        for entry in golden["hashContent"].values():
            assert gen.hash_content(entry["input"]).content_hash == entry["hash"], (
                f"ProofGenerator.hash_content diverged for {entry['input']!r:.40}"
            )

    def test_default_algorithm_is_keccak(self) -> None:
        assert ProofGenerator()._algorithm == "keccak256"


class TestComputeOutputHash:
    def test_string_deliverable_is_json_quoted(self, golden: dict) -> None:
        # compute_output_hash("hello") must equal computeResultHash("hello")
        # (TS JSON-quotes a string before hashing).
        expected = golden["canonical"]["string_top"]["resultHash"]
        assert compute_output_hash("hello") == expected
        assert compute_output_hash("hello") == compute_result_hash("hello")

    def test_object_deliverable_matches(self, golden: dict) -> None:
        inp = CANONICAL_INPUTS["justification"]
        assert compute_output_hash(inp) == golden["canonical"]["justification"]["resultHash"]


class TestAIP2QuoteSigning:
    def _quote(self, golden: dict) -> QuoteMessage:
        return QuoteMessage.from_dict(golden["quote"]["quote"])

    def test_justification_hash_matches(self, golden: dict) -> None:
        qb = QuoteBuilder()
        assert (
            qb.compute_justification_hash(golden["quote"]["justification"])
            == golden["quote"]["justificationHash"]
        )

    def test_compute_hash_matches_ts(self, golden: dict) -> None:
        qb = QuoteBuilder()
        assert qb.compute_hash(self._quote(golden)) == golden["quote"]["computeHash"]

    def test_eip712_signature_matches_ts(self, golden: dict) -> None:
        acct = Account.from_key(golden["quote"]["privateKey"])
        qb = QuoteBuilder(account=acct)
        quote = self._quote(golden)
        sig = qb.sign_quote(quote, golden["quote"]["kernelAddress"])
        assert sig == golden["quote"]["signature"], "EIP-712 quote signature diverged from TS"

    def test_verify_recovers_provider(self, golden: dict) -> None:
        # Isolate signature recovery (the cross-SDK crypto), not the wall-clock
        # business rules: the golden quote uses fixed past timestamps.
        acct = Account.from_key(golden["quote"]["privateKey"])
        qb = QuoteBuilder(account=acct)
        quote = self._quote(golden)
        quote.signature = qb.sign_quote(quote, golden["quote"]["kernelAddress"])
        recovered = qb._recover_quote_signer(quote, golden["quote"]["kernelAddress"])
        assert recovered.lower() == golden["quote"]["signerAddress"].lower()

    def test_signer_address_matches(self, golden: dict) -> None:
        acct = Account.from_key(golden["quote"]["privateKey"])
        assert acct.address.lower() == golden["quote"]["signerAddress"].lower()

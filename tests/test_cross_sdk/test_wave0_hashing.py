"""
Wave-0 cross-SDK byte-exactness parity tests.

These assert the Python SDK produces output BYTE-IDENTICAL to the TypeScript
SDK 4.8.0 for the protocol hashing/EIP-712 hot path. The golden vectors in
``tests/fixtures/cross_sdk/wave0_hashing.json`` are generated by the REAL TS
functions (``sdk-js/scripts/gen-wave0-vectors.cjs``):

- ``canonicalJsonStringify`` / ``computeResultHash``  (canonical JSON + keccak)
- ``ProofGenerator.hashContent``                      (keccak256(utf8))
- AIP-2 ``QuoteBuilder`` EIP-712 sign + ``computeHash``

A failure here means a Python agent and a TS agent would compute different
hashes/signatures for the same logical action — i.e. they could not interoperate
on-chain. Do not "fix" by regenerating without re-running the TS generator.
"""

